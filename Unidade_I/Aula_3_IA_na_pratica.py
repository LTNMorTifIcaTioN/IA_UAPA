# Agente Inteligente
"""
> Entidade que percebe o ambiente por meio de sensores e interage com este ambiente.
> Racional: toma as decisões corretas, isto implica, quando sua tabela de ações é preenchida corretamente.

É Racional se:
> A medida de desempenho que define o seu sucesso;
> Conhecimento prévio do ambiente;
> Ações que o agente é capaz de realizar;
> Sequência armazenada de percepções até o momento;
> Autonomia: capaz de tomar decisões quanto às ações a tomar sem a necessidade de ação externa do programador;
> Aprendizado: capaz de acumular conhecimento que seja útil em determinar suas ações, tornando-as cada vez mais
eficientes, segundo a medida de desempenho adotada.
"""